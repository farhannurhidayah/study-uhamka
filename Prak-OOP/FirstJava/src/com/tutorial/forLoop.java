package com.tutorial;

public class forLoop {
    public static void main(String[] args){
     System.out.println("Ini Program loop 1");
     for(int nilai = 0; nilai <= 10; nilai++) {
        System.out.println("Ini loop kee-" + nilai);
    }
     System.out.println("Ini Program loop 2");
     for(int nilai = 0; nilai < 10; nilai++) {
        System.out.println("Ini loop kee-" + nilai);
    }
     System.out.println("Ini Program loop 3");
     for(int nilai = 10; nilai >= 5; nilai--) {
        System.out.println("Ini loop kee-" + nilai);
    }
     System.out.println("Ini Program loop 4");
     for(int nilai = 0; nilai <= 10; nilai++) {
        System.out.println("Ini loop kee-" + nilai);
    }
    
    
    System.out.println("Selesai Program");
    }
}
